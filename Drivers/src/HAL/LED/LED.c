LED_ErrorStatus_t LED_init(void)
{
	LED_ErrorStatus_t LOC_state = LED_OK;
    GPIO_CFG_t LOC_gpiocfg;
    u32 LOC_loopCounter = 0;
    for (;LOC_loopCounter < LEDS_COUNT; LOC_loopCounter++)
    {
        LOC_gpiocfg.GPIO_Port = LED_cfg[LOC_loopCounter].port;
        LOC_gpiocfg.GPIO_Pin  = LED_cfg[LOC_loopCounter].pin;
        LOC_gpiocfg.GPIO_Mode = LED_cfg[LOC_loopCounter].mode;
        LOC_state = GPIO_init(&LOC_gpiocfg);
        
        GPIO_setReset(LED_cfg[LOC_loopCounter].port, LED_cfg[LOC_loopCounter].pin,(LED_cfg[LOC_loopCounter].connection ^ LED_cfg[LOC_loopCounter].inti_state));

    }
    return LOC_state;
}


LED_ErrorStatus_t LED_setState(LED_names_t ledName ,u8 state)
{
	LED_ErrorStatus_t LOC_errorState = LED_OK;
	LOC_errorState= GPIO_setReset(LED_cfg[ledName].port, LED_cfg[ledName].pin,(LED_cfg[ledName].connection ^ state));
	return LOC_errorState;
}


LED_ErrorStatus_t LED_toggleState(LED_names_t ledName)
{
	LED_ErrorStatus_t LOC_errorState = LED_OK;
	LOC_errorState = GPIO_toggle(LED_cfg[ledName].port,LED_cfg[ledName].pin);
	return LOC_errorState;
}

